alex_nue_scores = []
nova_nue_scores = []

for af, nf in zip(alex_files, nova_files):

  f_nova = ROOT.TFile.Open(nova_prefix + nf)
  f_alex = ROOT.TFile.Open(alex_prefix + af)


  # retrieve the ntuple of interest
  alex_chain = f_alex.Get('alex_net_cnn_tree')
  nova_chain = f_nova.Get('nova_net_cnn_tree')
  entries = alex_chain.GetEntriesFast()

  for jentry in xrange(entries):
      # get the next tree in the chain and verify
      ientry = alex_chain.LoadTree(jentry)
      if ientry < 0:
          break

      ientry = nova_chain.LoadTree(jentry)
      if ientry < 0:
          break

      # copy next entry into memory and verify
      nb = alex_chain.GetEntry(jentry)
      if nb <= 0:
          continue

      # copy next entry into memory and verify
      nb = nova_chain.GetEntry(jentry)
      if nb <= 0:
          continue

      # # use the values directly from the tree
      # nEvent = int(alex_chain.Event)
      # if nEvent < 0:
      #   continue

      alex_nue_scores.append(alex_chain.NueScore)
      nova_nue_scores.append(nova_chain.NueScore)


from matplotlib import pyplot as plt

bins = numpy.arange(0, 1, 0.01)

numpy.histogram(alex_nue_scores)

plt.hist2d(alex_nue_scores, nova_nue_scores, bins=bins, cmin=0.01)
